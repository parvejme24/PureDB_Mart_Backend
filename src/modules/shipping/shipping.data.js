// Helper functions
export const getDistrictsByDivision = (divisionId) => {
  return districts.filter((d) => d.divisionId === divisionId);
};

export const getUpazilasByDistrict = (districtId) => {
  return upazilas.filter((u) => u.districtId === districtId);
};

export const getDivisionById = (id) => {
  return divisions.find((d) => d.id === id);
};

export const getDistrictById = (id) => {
  return districts.find((d) => d.id === id);
};

export const getUpazilaById = (id) => {
  return upazilas.find((u) => u.id === id);
};

export const getDivisionByName = (name) => {
  return divisions.find((d) => d.name.toLowerCase() === name?.toLowerCase());
};

export const getDistrictByName = (name, divisionId) => {
  if (divisionId) {
    return districts.find(
      (d) =>
        d.name.toLowerCase() === name?.toLowerCase() &&
        d.divisionId === divisionId
    );
  }
  return districts.find((d) => d.name.toLowerCase() === name?.toLowerCase());
};

export const getUpazilaByName = (name, districtId) => {
  if (districtId) {
    return upazilas.find(
      (u) =>
        u.name.toLowerCase() === name?.toLowerCase() &&
        u.districtId === districtId
    );
  }
  return upazilas.find((u) => u.name.toLowerCase() === name?.toLowerCase());
};

// Format display name with both English and Bangla
export const formatDisplayName = (item, showBangla = true) => {
  if (!item) return "";
  if (showBangla && item.nameBn) {
    return `${item.name} (${item.nameBn})`;
  }
  return item.name;
};

